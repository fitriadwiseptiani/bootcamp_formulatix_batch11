namespace ExampleCalc;

public class Calculator
{
    public int Add(int a, int b)    //membuat fungsi untuk penjumlahan angka dengan 2 parameter integer
    {
        return (a + b); //mengembalikan hasil penjumlahan 2 angka
    }
    public int Multiply(int a, int b)   //fungsi untuk mengalikan 2 bilangan dengan 2 parameter  
    {
        return (a * b); //mengembalikan hasil perkalian 2 angka
    }
}
